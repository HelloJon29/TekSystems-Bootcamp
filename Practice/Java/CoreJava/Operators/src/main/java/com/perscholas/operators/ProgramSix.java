package com.perscholas.operators;
// Write a program that uses a postfix increment operator
public class ProgramSix {
    public static void main(String[] args) {
        //initialize variable at 0
        int x = 0;
        // print before incrementing
        System.out.println(x);
        // add the postfix increment to x
        x++;
        // Print the results
        System.out.println(x);
    }
}
